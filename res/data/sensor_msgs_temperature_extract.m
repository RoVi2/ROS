function [bag_data] = sensor_msgs_temperature_extract(filename)
fid = fopen(filename);
C = textscan(fid, ...
'%s%n%n%n%n%n', ...
'delimiter',',', ...
'headerLines', 1);
fclose(fid);
bag_data = struct(...
'seq', C{2}, ...
'nsecs', C{3}, ...
'secs', C{4}, ...
'times', C{4} + (C{3} / 1e9), ...
'temperature', C{5}, ...
'variance', C{6});
end